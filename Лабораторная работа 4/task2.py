def get_count_char(str_):
    word_dict = {}
    str_ = str_.lower()  # переводим в нижний регистр
    
    for el in str_:
        if el.isalpha():
            if word_dict.get(el, None):
                word_dict[el] += 1
            else:
                word_dict[el] = 1
    return word_dict

